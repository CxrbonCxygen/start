# 原码 反码 补码
# 正数的原码和反码一致，补码为原码
# 负数的原码不变，反码为符号位+原码的二进制取反
# 负数补码为原码的二进制取反+1
# 计算机运算存储的是补码
# -3的补码是11111111111111111111111111111101
# 1-3的运算实质是 1 + (-3)
# 即 00000000000000000000000000000001 + 11111111111111111111111111111101
# 结果为补码：11111111111111111111111111111110
# 输出为原码，符号位为1表示负数，数值部分取反加1
# 即：10000000000000000000000000000010，值为-2

# 位运算符
# & 按位与: 两个对应位都为1时结果为1，否则为0
# ~ 按位非: 对每个位进行取反操作
# | 按位或: 两个对应位中至少有一个为1时结果为1，否则为0
# ^ 按位异或: 两个对应位不同时结果为1，相同时为0
print(~-2)# -2的补码为11111111111111111111111111111110
# 取反：00000000000000000000000000000001（也是补码）
print(bin(~-2))
print(~2)# 2的补码为00000000000000000000000000000010
print(bin(~2))
# 取反：11111111111111111111111111111101（也是补码）
# 输出为原码（除符号位减一取反）：10000000000000000000000000000011
print("")
print(2 & 3)

# << 左移 >> 右移
# 左移：将二进制数左移n位，相当于乘以2的n次方
# 符号位不变，高位丢弃，低位补0
# 右移：将二进制数右移n位，相当于除以2的n次方
# 符号位不变，低位溢出，高位补符号位
